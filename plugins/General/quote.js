import axios from 'axios';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';

export default {
    name: 'quote',
    aliases: ['inspire', 'motivation', 'qotd'],
    description: 'Get a random motivational quote',
    run: async (context) => {
        const { client, m } = context;
        const fq = getFakeQuoted(m);
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
        try {
            const res = await axios.get('https://zenquotes.io/api/random', { timeout: 8000 });
            const q = res.data?.[0];
            if (!q) throw new Error('empty');
            await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
            return client.sendMessage(m.chat, {
                text: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« QбҙңбҙҸбҙӣбҙҮ вүӘв”Ғв”Ғв”Ғ\nв”ң\nв”ң вққ ${q.q} вқһ\nв”ң\nв”ң вҖ” ${q.a}\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`
            }, { quoted: fq });
        } catch {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return client.sendMessage(m.chat, { text: 'в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« QбҙңбҙҸбҙӣбҙҮ вүӘв”Ғв”Ғв”Ғ\nв”ң\nв”ң No quotes today. Universe is offline.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ' }, { quoted: fq });
        }
    }
};
